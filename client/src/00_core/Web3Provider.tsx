export const Web3Provider = ({ children }: { children: React.ReactNode }) => {
	const [web3, setWeb3] = useState<Web3 | null>(null);
	const [account, setAccount] = useState<string | null>(null);
	const [contract, setContract] = useState<any>(null);
  
	useEffect(() => {
	  const loadWeb3 = async () => {
		if ((window as any).ethereum) {
		  const web3Instance = new Web3((window as any).ethereum);
		  setWeb3(web3Instance);
  
		  try {
			await (window as any).ethereum.request({ method: "eth_requestAccounts" });
			const accounts = await web3Instance.eth.getAccounts();
			setAccount(accounts[0]);
  
			// 컨트랙트가 정상적으로 생성되는지 확인
			if (!ContractABI || !CONTRACT_ADDRESS) {
			  throw new Error("ABI 또는 컨트랙트 주소가 설정되지 않음!");
			}
  
			const contractInstance = new web3Instance.eth.Contract(
			  ContractABI,
			  CONTRACT_ADDRESS
			);
  
			if (!contractInstance) {
			  throw new Error("컨트랙트 인스턴스를 생성하지 못함!");
			}
  
			setContract(contractInstance);
			console.log("컨트랙트 로드 완료:", contractInstance);
		  } catch (error) {
			console.error("메타마스크 연결 실패:", error);
		  }
		} else {
		  console.warn("메타마스크가 설치되지 않음!");
		}
	  };
  
	  loadWeb3();
	}, []);
  
	return (
	  <Web3Context.Provider value={{ web3, account, contract }}>
		{children}
	  </Web3Context.Provider>
	);
  };

// Web3 컨텍스트 사용 훅
export const useWeb3 = () => useContext(Web3Context);